package de.pribluda.pirate.barser.beans;

/**
 * represents certain amt
 *
 * @author Konstantin Pribluda
 */
public class Amt {

    String name;
    String number;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }
}
